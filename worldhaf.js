console.log('Ini adalah contoh penggunaan let')
let x, y;
x = 7 + 9;
y = x + 5;

console.log('Nilai dari x adalah ' + x);
console.log('Nilai dari y adalah  ' + y);
// diatas adalah contoh penggunaan keyword let

console.log('Ini adalah contoh penggunaan var')
var m, n;
m = 17 / 5;
n = m * 2;
console.log('Nilai dari m adalah ' + m);
console.log('Nilai dari n adalah  ' + n);
// diatas adalah contoh penggunaan keyword var, sama seperti let

console.log('Ini adalah contoh penggunaan Const')
const a = 7 - 3;
const b = a - 1;

console.log('Nilai dari a adalah ' + a);
console.log('Nilai dari b adalah ' + b);
// diatas adalah contoh penggunaan keyword const, bedanya adalah const harus menggunakan fixed value atau nilai setelahnya

console.log('Ini adalah contoh penggunaan nothing')
q = 2 + 4;
r = q + 3;

console.log('Nilai dari q adalah ' + q);
console.log('Nilai dari r adalah ' + r);
// diatas adalah contoh penggunaan keyword nothing, atau tidak menggunakan apa apa